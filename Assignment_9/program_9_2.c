int DollarToInr(int iNo)
{
    int iRes = 0;
    if(iNo <= 0)
    {
        iNo = -iNo;
    }

    iRes = iNo * 70;
    return iRes;
}   // End of DollarToInr

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iAns = 0;
    printf("Enter Number :");
    scanf("%d",&iValue);

    iAns = DollarToInr(iValue);

    printf("Value to INR is %d",iAns);

}   // End of Main

