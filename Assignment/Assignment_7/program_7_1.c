//
/////////////////////////////////////////////////////////////////

void DisplayStar(int iNo)
{
    int iCnt = 0;

    if(iNo <= 0)
    {
        iNo = -iNo;
    }

    for(iCnt =1 ; iCnt <= iNo ; iCnt++)
    {
        printf("$\t*\t");
    } 

}   // End of DisplayStar

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter Number :");
    scanf("%d",&iValue);

    DisplayStar(iValue);

}   // End of Main

