/////////////////////////////////////////////////////////////////
//
//  Function Name : DisplayMultipleNumber
//  Description :   Display 5 multiple of number
//  Input :         int
//  output :        int
//  Author :        Ajinkya Rajendra Ghag
//  Date :          2/11/2025
//
/////////////////////////////////////////////////////////////////

void DisplayMultipleNumber(int iNo)
{
    int iCnt = 0;

    if(iNo <=0 )
    {
        iNo = -iNo;
    }
    for(iCnt = iNo ; iCnt <= (iNo*5) ; iCnt+=iNo)
    {
        printf("%d \t",iCnt);
    } 
}   // End of DisplayMultipleNumber

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter Number :");
    scanf("%d",&iValue);

    DisplayMultipleNumber(iValue);

}   // End of Main

