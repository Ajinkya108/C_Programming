void Factors(int iNo1)
{
    int iCnt = 0;

    if(iNo1 <= 0)
    {
        iNo1 = -iNo1;
    }
    
    for(iCnt = 1 ; iCnt <= iNo1 ; iCnt++)
    {
        if(iNo1 % iCnt == 0)
        {
            printf("%d\t",iCnt);
        }
    }
}   // End of Factors

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    printf("Enter a number:");
    scanf("%d",&iValue);

    Factors(iValue);

    return 0;
}   // End of Main

