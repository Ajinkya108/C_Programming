/////////////////////////////////////////////////////////////////

void DisplayOddNumber(int iNo)
{
    int iCnt = 0;

    if(iNo <=0 )
    {
        iNo = -iNo;
    }
    for(iCnt = 1 ; iCnt <= iNo ; iCnt+=2)
    {
        printf("%d \t",iCnt);
    } 
}   // End of DisplayOddNumber

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter Number :");
    scanf("%d",&iValue);

    DisplayOddNumber(iValue);

}   // End of Main

