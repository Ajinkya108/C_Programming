void Display()                 // Input
{
    int iCnt = 0;           
    iCnt = 5;
    while(iCnt >= 1)            //condtion
    {
        printf("%d\n",iCnt);    // Business logic
        iCnt--;                 // decrementor
    }
}   // End of Display

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{

    Display();                  // Method Call

    return 0;
}   // End of Main

